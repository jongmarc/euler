sub isPandigital{
	my $number = shift;
	return 0 if ( $number =~ /0/ || $number < 1 ); 
	my @unique = keys { map { $_ => 1 } split //, $number };
	return ( $#unique + 1 == length($number) );
}


sub isPrime {
    my $number = shift;
    if ($number==1) {return 0};
    if ($number==2) {return 1};
    my $d = 2;
    my $sqrt = sqrt $number;
    while(1) {
        if ($number%$d == 0) {
            return 0;
        }
        if ($d < $sqrt) {
            $d++;
        } else {
            return 1;
        }
    }
}

